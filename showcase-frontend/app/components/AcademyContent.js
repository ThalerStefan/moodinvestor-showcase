        <p>
        Beim langfristigen Investieren funktioniert das Prinzip ebenfalls. Man entscheidet bewusst, wie viel Prozent des Portfolios in risikoreiche Assets wie Bitcoin, Einzelaktien oder Altcoins fließen dürfen.
        </p>

